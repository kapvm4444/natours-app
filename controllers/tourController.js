// Get all the locations of tours, from the pin point [near by basis]
exports.getDistances = catchAsync(async (req, res, next) => {
  const { latlng, unit } = req.params;
  const [lat, lng] = latlng.split(',');
  const multiplier = unit === 'mi' ? 0.000621371 : 0.001;

  if (!lat || !lng)
    return next(
      new AppError(
        'Please Provide tha latitude and longitude in this format [lat, lng]',
        400,
      ),
    );

  //in order to get specific data from each record, use pipeline
  const distances = await Tour.aggregate([
    {
      //Geo near is the one and only aggregate stage for geo data [It should be always the first stage]
      // !important
      $geoNear: {
        near: {
          type: 'Point',
          coordinates: [lng * 1, lat * 1],
        },
        //specify the name of that distance field
        distanceField: 'distance',
        //set multiplier for distance unit [by default it will be displayed in meter so use multiplier]
        distanceMultiplier: multiplier,
      },
    },
    {
      $project: {
        distance: 1,
        name: 1,
      },
    },
  ]);

  res.status(200).json({
    status: 'success',
    data: {
      distances,
    },
  });
});
